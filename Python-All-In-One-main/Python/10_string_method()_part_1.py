# String Method : here are multiple string method.

name = "ShUbHaM  mISHra" #here 2 spaces between the string

# 1. len() function: it count the spaces also
print(f"using the len() function find the lenth of the string include the spaces also. {len(name)}")

#2. lower() method: convert the string in lower_case
print(f'lowercase of {name} is using lower function "{name.lower()}"')

# 3. upper() method : convert the string in UPPER CASE
print(f"converting {name} into upper case {name.upper()}")

#4. title() method : make first character of the letter in Capital letter
print(f"using title() methid in {name} is {name.title()}")

#5. count() method: this method count() the number of time letter repeated in the string
print(f"using count() method {name} the letter H repeaded is {name.count('H')} times.")